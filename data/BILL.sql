CREATE TABLE BILL(
    FEE_NUMBER NUMBER(20) NOT NULL,
    DETAILED_NUMBER NUMBER(20) NOT NULL,
    NAME VARCHAR2(10) NOT NULL,
    BILL_YEAR_MONTH NUMBER(20) NOT NULL,
    CALCUL_START_DATE DATE NULL,
    CALCUL_END_DATE DATE NOT NULL,
    CUTOFF_DATE DATE NOT NULL,
    IMPORT_STATUS VARCHAR2(10),
    IMPOSE_FINISH CHAR(1),
    ARREARS NUMBER(20),
    ARREARS_FINE NUMBER(20),
    AFTER_AMOUNT NUMBER(20),
    VBANK VARCHAR2(20) NOT NULL,
    PAYMENT_METHOD_ID VARCHAR2(10),
    AMOUNT NUMBER(20),
    LATE_FEE NUMBER(20),
    ADDRESS VARCHAR2(20)
);