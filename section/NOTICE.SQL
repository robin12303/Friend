CREATE TABLE NOTICE (
    NTC_NUMBER    NUMBER(20)     NOT NULL,
    NTC_TITLE    VARCHAR2(100)    NOT NULL,
    NTC_TXT    VARCHAR2(2000)    NOT NULL,
    NTC_WRITER    VARCHAR2(100)    NOT NULL,
    NTC_DATE    DATE    NOT NULL,
    NTC_CHANGER    VARCHAR2(100)    NOT NULL,
    NTC_CHANGE_DATE    DATE	NOT NULL,
    NTC_RESERVATION_NOTICE    DATE    NOT NULL
);