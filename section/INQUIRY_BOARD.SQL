CREATE TABLE INQUIRY_BOARD (
        B_NUMBER NUMBER(20)  NOT NULL,
        B_TITLE VARCHAR2(100) NULL,
        B_PW	NUMBER(20)  NOT NULL,
        B_WRITER VARCHAR2(15)	NOT NULL,
        B_CONTENT	VARCHAR(2000) NOT NULL,
        B_DATE DATE NOT NULL,
        LOCK_FLAG	CHAR(1) NOT NULL,
        B_RE_WRITER VARCHAR2(15) NOT NULL,
        B_RE_DATE DATE NOT	NULL
);